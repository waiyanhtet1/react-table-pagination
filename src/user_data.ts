export const getUsers = (page: number, limit: number, search: string) => {
  const array = [];
  // 0 - 4
  // 5 - 9
  if (search !== "") {
    const results = users
      .filter((user) => {
        return user.full_name
          .toLocaleLowerCase()
          .match(search.toLocaleLowerCase());
      })
      .slice((page - 1) * limit, page * limit);

    // searchLength(results.length);

    return results;
  } else {
    for (let i = (page - 1) * limit; i < page * limit && users[i]; i++) {
      array.push(users[i]);
    }
    return array;
  }
};

